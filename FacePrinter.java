public class FacePrinter {
    public static void main(String[] args) {
        System.out.println("  /////  ");  // Print the hair
        System.out.println(" | o o | ");  // Print the eyes
        System.out.println("(|  ^  |)");  // Print the ears and nose
        System.out.println(" | [_] | ");  // Print the mouth
        System.out.println("  -----  ");  // Print the chin
    }
}
